const STORAGE_KEY = 'cheersin_who_undercover_custom'

export function loadCustomWordPairs(): [string, string][] {
  if (typeof window === 'undefined') return []
  try {
    const raw = localStorage.getItem(STORAGE_KEY)
    if (!raw?.trim()) return []
    const parsed = JSON.parse(raw) as [string, string][]
    return Array.isArray(parsed) ? parsed.filter((p) => Array.isArray(p) && p.length === 2 && typeof p[0] === 'string' && typeof p[1] === 'string') : []
  } catch {
    return []
  }
}

export function saveCustomWordPairs(pairs: [string, string][]): void {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(pairs))
  } catch {
    /* ignore */
  }
}
